
public class Agencia {
	long numero;
	String nome;
	
	public Agencia(long numero, String nome) {
		this.numero = numero;
		this.nome = nome;
	}
	public long getNumero() {
		return numero;
	}
	public void setNumero(long numero) {
		this.numero = numero;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
}
